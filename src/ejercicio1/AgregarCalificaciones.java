    /**
     * Metodo para guardar elementos 
     * @param evt 
     */
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        String materia = txtMateria.getText();  
        String semestre = txtSemestre.getText();
        String calificaciones = txtCalificacion.getText();
        
        if( materia.trim().isEmpty() 
                || semestre.trim().isEmpty() 
                || calificaciones.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Necesitas completar los campos",
                    "Error", JOptionPane.WARNING_MESSAGE);
            return; 
        }
        
        KardexDatos.datos[indexModificar][0] = materia; 
        KardexDatos.datos[indexModificar][1] = semestre;
        KardexDatos.datos[indexModificar][2] = calificaciones;
        if(!updated) KardexDatos.index ++; 
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

